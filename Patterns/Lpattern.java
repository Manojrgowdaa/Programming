class Lpattern{
    public static void main(String[] args) {
        int height=5;
        for(int i=0;i<=height;i++){
        if(i==height-1){
          for(int j=0;j<height-1;j++){
            System.out.print("* ");
          }
        }else{
            System.out.println("* ");

        }

    }
}
}